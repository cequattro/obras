package pe.gob.sunafil.postulacioncas.bean;

import java.io.Serializable;

public class TipoIdentificiacionReclamo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4026227509585994974L;
	
	private String n_codtipide;
	private String v_destipide;
	private String v_flgact;
	
	public String getN_codtipide() {
		return n_codtipide;
	}
	public void setN_codtipide(String n_codtipide) {
		this.n_codtipide = n_codtipide;
	}
	public String getV_destipide() {
		return v_destipide;
	}
	public void setV_destipide(String v_destipide) {
		this.v_destipide = v_destipide;
	}
	public String getV_flgact() {
		return v_flgact;
	}
	public void setV_flgact(String v_flgact) {
		this.v_flgact = v_flgact;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	@Override
	public String toString() {
		return "TipoIdentificiacionReclamo [n_codtipide=" + n_codtipide
				+ ", v_destipide=" + v_destipide + ", v_flgact=" + v_flgact
				+ "]";
	}
	
	
}
