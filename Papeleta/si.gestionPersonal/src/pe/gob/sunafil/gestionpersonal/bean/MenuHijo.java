package pe.gob.sunafil.gestionpersonal.bean;

import java.io.Serializable;

/**
 * Bean que almacena todos los atributos del Menu Hijo de cada Sistema.
 */
public class MenuHijo implements Serializable {
        
    /**
     * (String)Atributo que almacena el Nivel del menu.(1=Hijo)
     */
    public String n_nivel;
    /**
     * (String)Atributo que almacena el codigo del Menu Padre.
     */
    public String v_codpad;
    /**
     * (String)Atributo que almacena la descripcion(nombre) del Menu.
     */
    public String v_desmen;
    /**
     * (String)Atributo que almacena la URL del Menu.
     */
    public String v_url;
    /**
     * (String)Atributo que almacena el nombre de la imagen del menu.
     */
    public String v_icono;

	/**
	 * @return the n_nivel
	 */
	public String getN_nivel() {
		return n_nivel;
	}

	/**
	 * @param n_nivel the n_nivel to set
	 */
	public void setN_nivel(String n_nivel) {
		this.n_nivel = n_nivel;
	}

	/**
	 * @return the v_codpad
	 */
	public String getV_codpad() {
		return v_codpad;
	}

	/**
	 * @param v_codpad the v_codpad to set
	 */
	public void setV_codpad(String v_codpad) {
		this.v_codpad = v_codpad;
	}

	/**
	 * @return the v_desmen
	 */
	public String getV_desmen() {
		return v_desmen;
	}

	/**
	 * @param v_desmen the v_desmen to set
	 */
	public void setV_desmen(String v_desmen) {
		this.v_desmen = v_desmen;
	}

	/**
	 * @return the v_icono
	 */
	public String getV_icono() {
		return v_icono;
	}

	/**
	 * @param v_icono the v_icono to set
	 */
	public void setV_icono(String v_icono) {
		this.v_icono = v_icono;
	}

	/**
	 * @return the v_url
	 */
	public String getV_url() {
		return v_url;
	}

	/**
	 * @param v_url the v_url to set
	 */
	public void setV_url(String v_url) {
		this.v_url = v_url;
	}
    
      
    /**
     * Metodo que imprime los datos que contiene el Bean.
     */
    public String toString() {
        String cadena = null;
        cadena = "\n";
        cadena += " \tINFO - MenuHijoBean : (String)n_nivel => "+n_nivel+"\n";
        cadena += " \tINFO - MenuHijoBean : (String)v_codpad => "+v_codpad+"\n";
        cadena += " \tINFO - MenuHijoBean : (String)v_desmen => "+v_desmen+"\n";
        cadena += " \tINFO - MenuHijoBean : (String)v_url => "+v_url+"\n";        
        cadena += " \tINFO - MenuHijoBean : (String)v_icono => "+v_icono+"\n";             
        return cadena;
    }

    
}
