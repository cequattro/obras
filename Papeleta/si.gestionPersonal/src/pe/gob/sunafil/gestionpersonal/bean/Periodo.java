package pe.gob.sunafil.gestionpersonal.bean;

import java.io.Serializable;
import java.util.Date;

public class Periodo implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = -2940218230169839787L;
	
	private String  v_periodo; 
	private String  v_descripcion;
	private Date 	d_fecinicio;
	private Date    d_fecfin; 
	private String  v_flgactivo; 
	private String  v_observacion;
	
	public String getV_periodo() {
		return v_periodo;
	}
	public void setV_periodo(String v_periodo) {
		this.v_periodo = v_periodo;
	}
	public String getV_descripcion() {
		return v_descripcion;
	}
	public void setV_descripcion(String v_descripcion) {
		this.v_descripcion = v_descripcion;
	}
	public Date getD_fecinicio() {
		return d_fecinicio;
	}
	public void setD_fecinicio(Date d_fecinicio) {
		this.d_fecinicio = d_fecinicio;
	}
	public Date getD_fecfin() {
		return d_fecfin;
	}
	public void setD_fecfin(Date d_fecfin) {
		this.d_fecfin = d_fecfin;
	}
	public String getV_flgactivo() {
		return v_flgactivo;
	}
	public void setV_flgactivo(String v_flgactivo) {
		this.v_flgactivo = v_flgactivo;
	}
	public String getV_observacion() {
		return v_observacion;
	}
	public void setV_observacion(String v_observacion) {
		this.v_observacion = v_observacion;
	}
	
	

}
